    (
        "Vision-Language-Action models",
        "generalist robot policies",
        (
            "VLA — Foundations & Training",
            "VLA — Reasoning, Planning & Inference",
            "VLA — Spatial Grounding & Embodiment",
            "VLA — Evaluation, Benchmarks & Safety",
        ),
    ),
    (
        "World Models",
        "learned models of environment dynamics",
        (
            "World Models — Control & Decision-Making",
            "World Models — Video Generation",
            "World Models — Interactive Simulation",
            "World Models — Object-Centric",
        ),
    ),
    (
        "3D & spatial perception",
        "",
        (
            "Open-Vocabulary Vision",
            "SLAM",
            "Semantic Mapping",
            "3D Reconstruction — Feed-Forward",
            "3D Generation",
        ),
    ),
    (
        "Reasoning, language & embodiment",
        "",
        (
            "Multimodal Reasoning",
            "Joint Embedding Predictive Architectures",
            "Vision-Language Navigation",
            "Human-to-Robot Demonstration Transfer",
            "Humanoid Control",
        ),
    ),
