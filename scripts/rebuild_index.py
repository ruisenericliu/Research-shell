"""Rebuild per-category ``index.json`` files from note frontmatter.

Walks ``Library/<Topic>/*.md`` (excluding ``MOC.md``), backfilling YAML
frontmatter for notes that lack it (parsed from the note's heading and meta
lines, plus the topic MOC's legacy ``## Papers`` blurbs), then regenerates
each topic's ``index.json`` — the machine-readable enumeration of filed
papers that the pipeline reads for dedup and the ~20-paper cap.

It also regenerates the auto-managed "What's in the library" block in
``README.md`` (between the ``library-overview`` markers) from the live
per-topic counts, so the README's snapshot never drifts from the indexes.

Run with ``--check`` to report drift without writing anything (verification).
"""

from __future__ import annotations

import argparse
import datetime as dt
import json
import logging
import re
from collections.abc import Mapping
from pathlib import Path

import yaml

REPO_ROOT = Path(__file__).resolve().parent.parent
LIBRARY_DIR = REPO_ROOT / "Library"
INDEX_NAME = "index.json"
MOC_NAME = "MOC.md"

README_PATH = REPO_ROOT / "README.md"
README_BEGIN = (
    "<!-- BEGIN library-overview "
    "(auto-generated by scripts/rebuild_index.py — do not edit by hand) -->"
)
README_END = "<!-- END library-overview -->"

# Editorial grouping of topics into families for the README overview, in
# render order. The umbrella-hub topics (``Vision Language Action Models``,
# ``World Models``) carry no papers of their own — they head a family of
# sub-topics. Topic counts are read live from the indexes; only the grouping
# and one-line descriptions are maintained here. Any Library topic missing
# from this map still surfaces in the README (under "Other topics" if it has
# papers, or the seeded-but-empty line if it does not), so a newly created
# topic never silently vanishes from the overview.
